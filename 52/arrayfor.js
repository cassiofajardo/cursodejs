// Percorrendo uma array com o uso de for.

const frutas = ['Maçã', 'Banana', 'Morango', 'Limão',
 'Melancia', 'Abacate', 'Uva'];
for (let i = 0; i < frutas.length; i++) {
    console.log(`Índice ${i}`, frutas[i]);
}

