
// Arrow function possibilita fazer uma função com uma simples linha de código, foram eliminados até mesmo a palavra function, os colchetes, return e parênteses do parâmetro. //
const raizQuadrada = numero => numero ** 0.5 
console.log(raizQuadrada(9));
console.log(raizQuadrada(16));
console.log(raizQuadrada(34));


 